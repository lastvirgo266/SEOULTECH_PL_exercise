package exercise;

public class ex25_ExplicitTypeConversion {
	   public static void main(String[] args) {
	         int x;
	         float y, z;
	         char c='A';

	         x = 7 / 2;
	         y = (float) 7 / 2;
	         z = 7 / 2;   
	         System.out.println("x = " + x + " y = " + y + " z = " + z);
	         c++;
	         System.out.println("c = " + c);
	   }
}
